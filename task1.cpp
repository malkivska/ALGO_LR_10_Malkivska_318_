// Метод для перевірки коректності часу у форматі 24 години
int TTime::CheckTime() {
    int code = 0;             // Змінна для коду помилки, 0 означає без помилок
    if (hour < 0 || hour > 23) code += 100;    // Перевірка годин, якщо поза діапазоном - додаємо 100 до коду
    if (min < 0 || min > 59) code += 10;       // Перевірка хвилин, якщо поза діапазоном - додаємо 10 до коду
    if (sec < 0 || sec > 59) code += 1;        // Перевірка секунд, якщо поза діапазоном - додаємо 1 до коду
    return code;             // Повертаємо код помилки (0, якщо все правильно)
}

// Метод для додавання однієї хвилини до часу з урахуванням переходу
void TTime::NextMin() {
    if (CheckTime() == 0) {       // Виконуємо лише якщо час коректний (код помилки 0)
        min++;                    // Додаємо одну хвилину
        if (min == 60) {          // Якщо хвилин стало 60 — скидаємо до 0
            min = 0;
            hour++;               // І додаємо одну годину
            if (hour == 24)       // Якщо годин стало 24 — скидаємо до 0 (початок нового дня)
                hour = 0;
        }
    }
}

